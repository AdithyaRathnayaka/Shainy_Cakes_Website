        			<h2>Process of Making</h2>
        			<p>A stand mixer makes this vanilla cake a breeze to make, though it can be made with a handheld electric beater. While it takes time to beat the eggs to make a lovely fluffy cake, this recipe is more fail-proof than the usual “cream butter and sugar until fluffy” recipes </p>
        			<p>Whip the eggs and sugar – Beat the eggs with sugar for a whole 7 minutes until thick and glossy, it changes from yellow to white, and tripled in volume (just over double depth in the bowl).

The aeration created during this step is key for a soft, fluffy crumb. This is a technique borrowed from Japanese sponge cakes.</p>
					<p>Gradually add flour – Whisk together the flour, baking powder and salt. Then add into egg mixture in 3 lots, mixing for just 5 seconds on Speed 1 in between. This should make the flour just incorporated – don’t worry if there are some bits not mixed in.</p>
					<p>Hot melted butter in milk – use a microwave or stove to heat milk with the butter until it melts. Believe it or not, HOT milk helps with aeration in this cake. I doubted it, so I tried with cooled melted butter in milk and found it did not rise as well.
The use of hot milk with melted butter in a cake batter is another technique used in some types of Japanese sponge cakes (such as this one from Chopstick Chronicles, a Japanese food blog). Interestingly, I recently discovered it is also used in a traditional Southern cake called Hot Milk Cakes.</p>
					<p>Mix some batter into hot milk – this serves two purposes: a) bring down the temperature of the hot milk so it won’t “cook” the eggs (ie. tempering); and b) it lightens up the density of the hot milk (see above – it becomes foamy) so it blends together faster when added to the rest of the whipped egg mixture.</p>
					<p>Slowly pour milk mixture back into whipped eggs over 20 seconds on Speed 1. Scrape down sides of bowls, then mix for 10 seconds on Speed 1 – the batter should now be smooth;</p>
					<p>Pour into 2 cake pans lined with parchment/baking paper.</p>
					<p>Bake 30 minutes until golden brown and a toothpick inserted into the middle comes out clean. The cake will have a slight dome but it flattens when it cools.</p>
					<p>COOL upside down for level cakes (so you have neat, straight layers in a layer cake) on a cooling rack. If you don’t mind a slight dome surface / specifically want to show the beautiful golden brown surface (eg if making a cake dusted with icing sugar / powdered sugar, or a glaze), then cool it right side up; and</p>
					<p>Decorate! Frost with your favourite frosting, smother with cream, fresh berries, swirl with chocolate fudge frosting or simply dust with icing sugar and serve with a dollop of yogurt or cream!</p>
